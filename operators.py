
#Arithmetic Operators - Simple Calculations
num1 = 45
num2 = 20

print(num1 + num2) #Addition
print(num1 - num2) #Subtraction
print(num1 * num2) #Multiplication
print(num1 / num2) #Division
print(num1 % num2) #Modulus - Returns remainder

#Comparison Operators
x = 78
y = 60

print(x < y ) #less than
print(x > y ) #greater than
print(x <= y ) #less than or equal to
print(x >= y ) #greater than or equal to
print(x == y ) #equal to
print(x != y ) #not equal to

#Assignement Operators - used to assign values to variables
number = 40 #assignment operator
print(number)

a = 35
a += 2
print(a)

result = 90 - (6 *3)/2 + 5 #Operetor precedence
print(result)

#Logical Operators - and, or, not
print(90<40 and 67>34)
print(90<40 or 67>34)
print(not (90<40 or 67>34))

